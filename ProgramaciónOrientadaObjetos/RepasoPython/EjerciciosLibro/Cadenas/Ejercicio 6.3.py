'''Ejercicio 6.3.Investigar qué signiﬁcan las notaciones a[:j] y a[j:] si j es un número negativo.'''

cadena = input("Ingrese una cadena de caracteres: ")

print(cadena[:-2]) #imprime todos los caracteres anteriores a la posición -2, comenzando desde el ppio de la cadena
print(cadena[-4:]) #imprime todos los caracteres desde la posición -4 hasta el final de la cadena